const typeDefs = `
  type Author {
    name: String!
    born: String
    id: ID!
    bookCount: Int
  }

  type Book {
    title: String!
    published: Int!
    author: Author!
    id: ID!
    genres: [String!]!
  }

  type Query {
    bookCount: Int!
    authorCount: Int!
    allBooks(
      author: String
      genre: String
    ): [Book]!
    allAuthors: [Author!]!
  }

  type Mutation {
    addBook(
      title: String!
      author: String!
      published: Int!
      genres: [String!]!
    ): Book

    editAuthor(
      name: String!
      setBornTo: Int!
    ): Author
  }
`

const resolvers = {
  Query: {
    bookCount: async () => Book.collection.countDocuments(),
    authorCount: async () => Author.collection.countDocuments(),

    allBooks: async (root, args) => {
      /*
      return books
        .filter(book => args.author ? (book.author === args.author) : book)
        .filter(book => args.genre ? book.genres.includes(args.genre) : book)
      */
      return Book.find({}).populate('author')
    },

    allAuthors: async () => {
      return Author.find({})
    }
  },
  Author: {
    // Part1 - BookCount doesnt have to work
    bookCount: (root) => {
      const bookCountByAuthor = _.countBy(books.map(book => book.author))
      const uniqueAuthors = _.keys(bookCountByAuthor)
      const bookCounts = _.values(bookCountByAuthor)

      const authorAndCountObj = uniqueAuthors.reduce(
        (result, author, index) => {
          result.push({
            name: author,
            bookCount: bookCounts[index]
          })
          return result
        }, [])

      return authorAndCountObj.find(authorAndBookCount => authorAndBookCount.name === root.name).bookCount
    }
  },
  Book: {
    author: (root) => {
      return authors.find(author => author.name === root.author)
    }
  },
  Mutation: {
    addBook: async (root, args) => {
      console.log('In addBook mutation!')
      try {
        // If author doesn't yet exist, add it
        let author = await Author.findOne({ name: args.author })
        if (!author) {
          console.log('Creatin a new author')
          author = new Author({ name: args.author })
          console.log('New author', author)
          await author.save()
        }
        // Create the book
        const book = new Book({
          title: args.title,
          published: args.published,
          author: author._id, // Reference to the author id
          genres: args.genres
        })

        await book.save()
        return book

      } catch (error) {
        console.log(error)
        throw new GraphQLError('Saving book failed', {
          extensions: {
            code: 'BAD_USER_INPUT',
            invalidArgs: args.name,
            error
          }
        })
      }
    },
    // Part1- editAuthor doesn't have to work
    editAuthor: (root, args) => {
      const author = authors.find(author => author.name === args.name)
      if (!author) {
        return null
      }

      const updatedAuthor = { ...author, born: args.setBornTo }
      authors = authors.map(author => author.name === args.name ? updatedAuthor : author)
      return updatedAuthor
    }
  }
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

